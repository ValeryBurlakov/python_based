from pathlib import Path
import json


def create_person(path_file: Path) -> None:
    set_id = set()
    if path_file.is_file():
        with open(path_file, 'r', encoding='utf=8') as f:
            data = json.load(f)
            print(data)
            for _, id_name in data.items():
                set_id.update(id_name.keys())
    else:
        data = {str(level): {} for level in range(1, 7 + 1)}

    while True:
        name = input("–í–≤–µ–¥–∏—Ç–µ –∏–º—è: ")
        if name == '':
            break
        id = input("–í–≤–µ–¥–∏—Ç–µ –∏–¥–µ–Ω—Ç–∏—Ñ–∏–∫–∞—Ç–æ—Ä: ")
        level = input("–í–≤–µ–¥–∏—Ç–µ —É—Ä–æ–≤–µ–Ω—å –¥–æ—Å—Ç—É–ø–∞ –æ—Ç 1 –¥–æ 7: ")
        if id not in set_id:
            data[level].update({id: name})
            set_id.add(id)
            with open(path_file, 'w', encoding='utf=8') as f:
                json.dump(data, f, ensure_ascii=False, indent=2)


if __name__ == '__main__':
    create_person(Path("users.json"))
